using System;
using System.Collections.Generic;
using System.Text;

namespace Intuit.Platform.Client.Core
{
    /// <summary>
    /// Query Comparision operator
    /// </summary>
    public enum QueryComparisionOperator
    {
        ///<summary>
        /// Contains
        /// </summary>
        CT,
        ///<summary>
        /// Does not contain
        /// </summary>
        XCT,
        ///<summary>
        /// Is
        /// </summary>
        EX,
        ///<summary>
        /// True Value (compares against the underlying foreign key or Record ID# stored in relationship fields). Also is used for queries on user fields.
        /// </summary>
        TV,
        ///<summary>
        ///Is not
        /// </summary>	
        XEX,
        ///<summary>
        ///Starts with
        /// </summary>
        SW,
        ///<summary>
        /// Does not start with
        /// </summary>
        XSW,
        ///<summary>
        ///Is before
        /// </summary>
        BF,
        ///<summary>
        ///Is on or before
        /// </summary>
        OBF,
        ///<summary>
        ///Is after
        /// </summary>
        AF,
        ///<summary>
        ///Is on or after
        /// </summary>
        OAF,
        ///<summary>
        ///Is less than
        /// </summary>
        LT,
        ///<summary>
        ///Is less than or equal to
        /// </summary>
        LTE,
        ///<summary>
        ///Is greater than
        /// </summary>
        GT,
        ///<summary>
        ///Is greater than or equal to
        /// </summary>
        GTE
    }
}

using System;

namespace Intuit.Common.Util
{
	/// <summary>
	/// A class that knows how to wrap exceptions. Used by some utitity code.
	/// </summary>
	public interface IExceptionWrapper
	{
		/// <summary>
		/// Wraps an exception into another exception if it's desirable. Returns null if no wrapping occured.
		/// </summary>
		Exception WrapException(Exception e);
	}
}
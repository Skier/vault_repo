namespace Intuit.Common.Util
{
	/// <summary>
	/// An interface for an Exception class that can suggest a text and a caption (like a message box title) that describes the error and is suitable to be displayed to end-users.
	/// </summary>
	public interface IExceptionWithDisplayText
	{
		/// <summary>
		/// An end-user facing description of the error.
		/// </summary>
		string MessageText { get; }

		/// <summary>
		/// An end-user facing caption for the description of the error (e.g. to be used as a message box title).
		/// </summary>
		string MessageCaption { get; }
	}
}